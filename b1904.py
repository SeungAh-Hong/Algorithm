# n=1, 1 (1)
# n=2, 00, 11 (2)
# n=3, 100, 001 / 111 (3)
# n=4, 0000 / 0011, 1001, 1100 / 1111 (5)
# n=5, 00001, 10000, 00100 / 00111, 10011, 11001, 11100 / 11111 (8)

n = int(input())
dp = [0] * (n+2)
dp[0] = 0
dp[1] = 1
dp[2] = 2

for i in range(3, n+1):
    dp[i] = dp[i-1] + dp[i-2]
    dp[i] %= 15746

print(dp[n])